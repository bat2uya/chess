void dursdraw(int a[],int x,int y,int color,int L)
{ setcolor(color); int X,Y,S=105;
  X=x;Y=y;
  switch(L)
  {
   case 1:S=95; break;
   case 2:S=123;break;
   case 3:S=111;break;
   case 4:S=103;break;
   case 5:S=105;break;
   case 6:S=105;break;

  }

  for (int j=0;j<S;j++)
   {
	  if(a[j]==0)
	     { X=x;Y++; }
	  else
	     {
	      if(a[j]!=0)
	       { X=X+a[j];
		 moveto(X,Y); j++;
		 X=X+a[j];
		 lineto(X,Y);
	       }
	     }
   }
}
